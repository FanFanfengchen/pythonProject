"""计算器模块

该模块提供简单的数学运算功能，目前包含加法运算。
"""

def my_adder(x, y):
    """执行两个数的加法运算
    
    Args:
        x: 第一个加数，可以是整数或浮点数
        y: 第二个加数，可以是整数或浮点数
    
    Returns:
        两个数的和，类型与输入参数一致
    
    Examples:
        >>> my_adder(5, 3)
        8
        >>> my_adder(2.5, 1.5)
        4.0
    """
    return x + y