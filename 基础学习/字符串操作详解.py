s = 'abc123456'
n = len(s)

print(f"原始字符串: '{s}'，长度: {n}")
print("=" * 50)

# 基本索引操作
print("1. 基本索引操作:")
print(f"print(s[0], s[-n])  -->  {s[0], s[-n]}")
print("   解释: s[0]是第一个字符 'a'，s[-n]即s[-9]也是第一个字符 'a'")

print(f"print(s[n-1], s[-1])  -->  {s[n-1], s[-1]}")
print("   解释: s[n-1]即s[8]是最后一个字符 '6'，s[-1]也是最后一个字符 '6'")

print(f"print(s[2], s[-7])  -->  {s[2], s[-7]}")
print("   解释: s[2]是第三个字符 'c'，s[-7]从后往前数第7个字符，同样是 'c'")

print(f"print(s[5], s[-4])  -->  {s[5], s[-4]}")
print("   解释: s[5]是第6个字符 '3'，s[-4]从后往前数第4个字符，同样是 '3'")
print()

# 切片操作
print("2. 切片操作 [start:end]:")
print(f"print(s[2:5])  -->  '{s[2:5]}'")
print("   解释: 从索引2开始到索引5之前结束，即获取索引2,3,4的字符 'c12'")

print(f"print(s[-7:-4])  -->  '{s[-7:-4]}'")
print("   解释: 从索引-7开始到索引-4之前结束，即获取索引-7,-6,-5的字符 'c12'")
print()

# 部分省略切片
print("3. 部分省略切片操作:")
print(f"print(s[2:])  -->  '{s[2:]}'")
print("   解释: 从索引2开始到字符串末尾")

print(f"print(s[:2])  -->  '{s[:2]}'")
print("   解释: 从字符串开头到索引2之前结束")
print()

# 步长切片
print("4. 步长切片操作 [start:end:step]:")
print(f"print(s[::2])  -->  '{s[::2]}'")
print("   解释: 从头到尾，每隔一个字符取一个字符")

print(f"print(s[::-1])  -->  '{s[::-1]}'")
print("   解释: 从尾到头，步长为-1，实现字符串反转")
print()

